package com.lfscheidegger.jfacet.activity.jfacet.shade;

/**
 * Namespace for deafult GLSL types.
 */
public class Types {

  /**
   * atomic floating-point value
   */
  public static final String FLOAT_T = "float";

  /**
   * atomic integer value
   */
  public static final String INT_T = "int";

  /**
   * atomic boolean value
   */
  public static final String BOOL_T = "bool";

  /**
   * 2-dimensional floating-point vector
   */
  public static final String VEC2_T = "vec2";

  /**
   * 3-dimensional floating-point vector
   */
  public static final String VEC3_T = "vec3";

  /**
   * 4-dimensional floating-point vector
   */
  public static final String VEC4_T = "vec4";

  /**
   * 2-dimensional integer vector
   */
  public static final String IVEC2_T = "ivec2";

  /**
   * 3-dimensional integer vector
   */
  public static final String IVEC3_T = "ivec3";

  /**
   * 4-dimensional integer vector
   */
  public static final String IVEC4_T = "ivec4";

  /**
   * 2-dimensional boolean vector
   */
  public static final String BVEC2_T = "bvec2";

  /**
   * 3-dimensional boolean vector
   */
  public static final String BVEC3_T = "bvec3";

  /**
   * 4-dimensional boolean vector
   */
  public static final String BVEC4_T = "bvec4";

  /**
   * 2x2-dimensional floating-point matrix
   */
  public static final String MAT2_T = "mat2";

  /**
   * 3x3-dimensional floating-point matrix
   */
  public static final String MAT3_T = "mat3";

  /**
   * 4x4-dimensional floating-point matrix
   */
  public static final String MAT4_T = "mat4";

  /**
   * opaque texture reference type
   */
  public static final String SAMPLER2D_T = "sampler2D";
}
